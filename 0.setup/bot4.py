print("which direction should I paint?")
direction = input()

if (direction == ("up")):
    print("\n Im painting in an upward direction")
elif (direction == ("right")):
    print("\n Im painting in the right direction")
elif (direction == ("down")):
    print("\n Im painting in the downward direction")
elif (direction == ("left")):
    print("\n Im painting in the left direction")
else :
    print("\nUnsure of direction")
